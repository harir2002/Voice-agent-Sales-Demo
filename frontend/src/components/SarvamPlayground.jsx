function SarvamPlayground() {
    const [text, setText] = useState('')
    const [voice, setVoice] = useState('anushka')
    const [language, setLanguage] = useState('hi-IN')
    const [audioQuality, setAudioQuality] = useState('22050')
    const [pace, setPace] = useState(1.0)
    const [pitch, setPitch] = useState(0)
    const [loudness, setLoudness] = useState(1.0)
    const [speechEnhance, setSpeechEnhance] = useState(true)
    const [isPlaying, setIsPlaying] = useState(false)
    const [currentAudio, setCurrentAudio] = useState(null)
    const [error, setError] = useState(null)
    const [isGenerating, setIsGenerating] = useState(false)

    const handleGenerate = async () => {
        if (!text.trim()) {
            setError("Please enter some text")
            return
        }

        setError(null)
        setIsGenerating(true)

        // Stop any current audio
        if (currentAudio) {
            currentAudio.pause()
            currentAudio.currentTime = 0
        }

        try {
            const response = await axios.post(`${API_BASE_URL}/tts/playground`, {
                text: text,
                speaker: voice,
                language_code: language,
                pace: pace
            })

            if (response.data.audio) {
                const audio = new Audio(`data:audio/mp3;base64,${response.data.audio}`)
                setCurrentAudio(audio)
                setIsPlaying(true)

                audio.onended = () => {
                    setIsPlaying(false)
                }

                audio.onerror = () => {
                    setError("Failed to play audio")
                    setIsPlaying(false)
                }

                audio.play()
                setIsGenerating(false)
            } else {
                setError("No audio returned from server")
                setIsGenerating(false)
            }
        } catch (err) {
            console.error("TTS Error:", err)
            setError(err.response?.data?.detail || "Failed to generate audio")
            setIsGenerating(false)
        }
    }

    const handleStop = () => {
        if (currentAudio) {
            currentAudio.pause()
            currentAudio.currentTime = 0
            setCurrentAudio(null)
        }
        setIsPlaying(false)
    }

    const handleReset = () => {
        setVoice('anushka')
        setLanguage('hi-IN')
        setAudioQuality('22050')
        setPace(1.0)
        setPitch(0)
        setLoudness(1.0)
        setSpeechEnhance(true)
    }

    const loadSample = (sampleText) => {
        setText(sampleText)
    }

    // Get current language samples
    const currentLangSamples = LANGUAGES.find(l => l.code === language)?.samples || []

    return (
        <div className="playground-page">
            {/* Left Sidebar - Preferences */}
            <motion.div
                className="preferences-panel"
                initial={{ opacity: 0, x: -20 }}
                animate={{ opacity: 1, x: 0 }}
                transition={{ duration: 0.4 }}
            >
                <div className="panel-header">
                    <h2>Preferences</h2>
                    <button className="reset-btn" onClick={handleReset} title="Reset to defaults">
                        <RefreshCw size={16} />
                    </button>
                </div>

                <div className="preference-group">
                    <label>LANGUAGE</label>
                    <select value={language} onChange={(e) => setLanguage(e.target.value)}>
                        {LANGUAGES.map(l => (
                            <option key={l.code} value={l.code}>{l.name}</option>
                        ))}
                    </select>
                </div>

                <div className="preference-group">
                    <label>VOICE</label>
                    <select value={voice} onChange={(e) => setVoice(e.target.value)}>
                        {SARVAM_VOICES.map(v => (
                            <option key={v.id} value={v.id}>{v.name}</option>
                        ))}
                    </select>
                </div>

                <div className="preference-group">
                    <label>AUDIO QUALITY</label>
                    <select value={audioQuality} onChange={(e) => setAudioQuality(e.target.value)}>
                        <option value="22050">22.05kHz (default)</option>
                        <option value="16000">16kHz</option>
                        <option value="8000">8kHz (phone quality)</option>
                    </select>
                </div>

                <div className="preference-group slider-group">
                    <label>PACE</label>
                    <div className="slider-container">
                        <span className="slider-label">Slow</span>
                        <input
                            type="range"
                            min="0.5"
                            max="2.0"
                            step="0.1"
                            value={pace}
                            onChange={(e) => setPace(parseFloat(e.target.value))}
                        />
                        <span className="slider-value">{pace.toFixed(1)}</span>
                        <span className="slider-label">Fast</span>
                    </div>
                </div>

                <div className="preference-group slider-group">
                    <label>PITCH</label>
                    <div className="slider-container">
                        <span className="slider-label">Low</span>
                        <input
                            type="range"
                            min="-10"
                            max="10"
                            step="1"
                            value={pitch}
                            onChange={(e) => setPitch(parseInt(e.target.value))}
                        />
                        <span className="slider-value">{pitch}</span>
                        <span className="slider-label">High</span>
                    </div>
                </div>

                <div className="preference-group slider-group">
                    <label>LOUDNESS</label>
                    <div className="slider-container">
                        <span className="slider-label">Low</span>
                        <input
                            type="range"
                            min="0.5"
                            max="2.0"
                            step="0.1"
                            value={loudness}
                            onChange={(e) => setLoudness(parseFloat(e.target.value))}
                        />
                        <span className="slider-value">{loudness.toFixed(1)}</span>
                        <span className="slider-label">High</span>
                    </div>
                </div>

                <div className="preference-group toggle-group">
                    <div className="toggle-info">
                        <label>SPEECH ENHANCE</label>
                        <p>Handles numbers, code, and multilingual content with clarity</p>
                    </div>
                    <label className="toggle-switch">
                        <input
                            type="checkbox"
                            checked={speechEnhance}
                            onChange={(e) => setSpeechEnhance(e.target.checked)}
                        />
                        <span className="toggle-slider"></span>
                    </label>
                </div>
            </motion.div>

            {/* Right Content - Text to Speech */}
            <motion.div
                className="tts-panel"
                initial={{ opacity: 0, x: 20 }}
                animate={{ opacity: 1, x: 0 }}
                transition={{ duration: 0.4, delay: 0.1 }}
            >
                <div className="tts-header">
                    <Volume2 size={28} />
                    <h1>Text to Speech</h1>
                    <span className="header-badge">Sarvam AI</span>
                </div>

                <div className="text-input-container">
                    <textarea
                        value={text}
                        onChange={(e) => setText(e.target.value)}
                        placeholder="Enter text to hear it loud..."
                        maxLength={2500}
                    />
                    <span className="char-count">{text.length} / 2500 Characters</span>
                </div>

                {error && (
                    <div className="error-banner">
                        ‚ö†Ô∏è {error}
                    </div>
                )}

                <div className="sample-ideas">
                    <p>üéØ Try these {LANGUAGES.find(l => l.code === language)?.name} samples:</p>
                    <div className="sample-buttons">
                        {currentLangSamples.map((sample, index) => (
                            <button
                                key={index}
                                className="sample-btn"
                                onClick={() => loadSample(sample)}
                            >
                                Sample {index + 1}
                            </button>
                        ))}
                    </div>
                </div>

                <div className="action-bar">
                    <button className="clear-text-btn" onClick={() => setText('')}>
                        <Sparkles size={16} />
                        Clear Text
                    </button>
                    <motion.button
                        className={`generate-btn ${isGenerating || isPlaying ? 'active' : ''}`}
                        onClick={isPlaying ? handleStop : handleGenerate}
                        disabled={isGenerating}
                        whileHover={{ scale: 1.02 }}
                        whileTap={{ scale: 0.98 }}
                    >
                        {isGenerating ? (
                            <>
                                <Loader2 className="spin" size={18} />
                                Generating...
                            </>
                        ) : isPlaying ? (
                            <>
                                <Square size={18} />
                                Stop
                            </>
                        ) : (
                            <>
                                <Play size={18} />
                                Generate
                            </>
                        )}
                    </motion.button>
                </div>

                <div className="current-settings">
                    <span>üéôÔ∏è Voice: <strong>{SARVAM_VOICES.find(v => v.id === voice)?.name}</strong></span>
                    <span className="divider">‚Ä¢</span>
                    <span>üåê Language: <strong>{LANGUAGES.find(l => l.code === language)?.name}</strong></span>
                    <span className="divider">‚Ä¢</span>
                    <span>‚ö° Pace: <strong>{pace}x</strong></span>
                </div>
            </motion.div>
        </div>
    )
}

export default SarvamPlayground
